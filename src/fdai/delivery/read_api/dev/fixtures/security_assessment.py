def _spec(
    control_id: str,
    title: str,
    category: str,
    status: str,
    severity: str,
    current_value: str,
    expected_value: str,
    source: str,
    **metadata: str,
) -> dict[str, Any]:
    return {
        "control_id": control_id,
        "title": title,
        "category": category,
        "status": status,
        "severity": severity,
        "current_value": current_value,
        "expected_value": expected_value,
        "source": source,
        **metadata,
    }


def _signal(spec: dict[str, Any], *, now: datetime, index: int) -> ReportSignal:
    control_id = str(spec["control_id"])
    mysql = control_id.startswith("mysql-")
    resource_type = "sql-database" if mysql else "kubernetes-cluster"
    resource_ref = "rg-example-data/mysql-example" if mysql else "rg-example-app/aks-example"
    severity = Severity(str(spec["severity"]))
    metadata = {
        "control_id": control_id,
        "control_category": str(spec["category"]),
        "status": str(spec["status"]),
        "resource_type": resource_type,
        "current_value": str(spec["current_value"]),
        "expected_value": str(spec["expected_value"]),
        "source": str(spec["source"]),
        "priority": str(spec.get("priority", "none")),
        "due_days": str(spec.get("due_days", "")),
        "remediation": str(spec.get("remediation", "")),
        "validation": str(spec.get("validation", "")),
        "applicability": str(spec.get("applicability", "applicable")),
        "cve_ids": str(spec.get("cves", "")),
        "compliance_controls": str(spec.get("compliance", "")),
        "source_urls": "https://example.com/security-guidance",
        "managed_service_note": str(spec.get("managed_note", "")),
        "patch_status": str(spec.get("patch_status", "not_assessed")),
    }
    return ReportSignal(
        signal_id=f"security-control-{index + 1:02d}",
        kind=SignalKind.SECURITY_ASSESSMENT,
        category=ReportCategory.SECURITY,
        severity=severity,
        resource_ref=resource_ref,
        title=str(spec["title"]),
        detail=f"Observed {spec['current_value']}; expected {spec['expected_value']}.",
        occurred_at=now,
        evidence_refs=(f"{spec['source']}:{control_id}",),
        metadata=metadata,
    )


