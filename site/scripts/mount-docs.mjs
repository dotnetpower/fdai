#!/usr/bin/env node
// mount-docs.mjs — link ../docs/roadmap into src/content/docs without copying.
//
// Layout produced (all entries are symlinks pointing back at docs/roadmap/**):
//   site/src/content/docs/roadmap/index.md         → docs/roadmap/README.md
//   site/src/content/docs/roadmap/foo.md           → docs/roadmap/foo.md
//   site/src/content/docs/roadmap/phases/foo.md    → docs/roadmap/phases/foo.md
//   site/src/content/docs/ko/roadmap/index.md      → docs/roadmap/README-ko.md
//   site/src/content/docs/ko/roadmap/foo.md        → docs/roadmap/foo-ko.md
//   site/src/content/docs/ko/roadmap/phases/foo.md → docs/roadmap/phases/foo-ko.md
//
// Why symlinks rather than a custom glob() loader with base='../docs/roadmap':
// Starlight's docsLoader() is fixed to src/content/docs/ (per Starlight repo
// packages/starlight/loaders.ts). Swapping in a raw glob() loader is workable
// in Astro 5 but the Starlight maintainer flagged three regressions with that
// approach: multilingual remark bugs, autogenerated sidebar groups not
// working, and lastUpdated relying on git times inside src/content/docs/.
// Symlinks keep Starlight's default loader (all three features work) without
// duplicating any content into the repo.
//
// The links are the site's presentation layer only. The source of truth is
// docs/roadmap/*.md; edits happen there. Symlink targets are gitignored so
// they never appear in commits or in the English-only / translation gates.

import { execFileSync } from "node:child_process";
import { mkdir, readdir, readFile, rm, stat, symlink, writeFile } from "node:fs/promises";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const siteRoot = resolve(scriptDir, "..");
const repoRoot = resolve(siteRoot, "..");
const docsSource = resolve(repoRoot, "docs", "roadmap");
// The roadmap now lives under /reference/roadmap/ on the site so that the
// user-facing sections (Get Started, Concepts, Guides) can occupy the top
// of the sidebar. The canonical Markdown at docs/roadmap/ is unchanged;
// only its mount location on the site moves.
const docsMountEn = resolve(siteRoot, "src", "content", "docs", "reference", "roadmap");
const docsMountKo = resolve(siteRoot, "src", "content", "docs", "ko", "reference", "roadmap");
const staleListPath = resolve(siteRoot, "src", "data", "stale-translations.json");

/**
 * Recursively walk `dir` and yield `{ absPath, relPath }` for each *.md file.
 * relPath is relative to `docsSource` so callers can decide where the link
 * lands.
 */
async function* walkMarkdown(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const abs = join(dir, entry.name);
    if (entry.isDirectory()) {
      yield* walkMarkdown(abs);
      continue;
    }
    if (!entry.name.endsWith(".md")) continue;
    yield { absPath: abs, relPath: relative(docsSource, abs) };
  }
}

/**
 * Compute the mounted path for a given source markdown file.
 * Returns null when the file should be skipped (nothing at present).
 */
function mountTargetFor(relPath) {
  // README.md and README-ko.md become the index page of their locale.
  // Everything else keeps its filename, minus the -ko suffix for Korean.
  const parts = relPath.split("/");
  const filename = parts[parts.length - 1];
  const isKo = filename.endsWith("-ko.md");
  const bareName = isKo ? filename.slice(0, -"-ko.md".length) : filename.slice(0, -".md".length);
  const finalName = bareName === "README" ? "index.md" : `${bareName}.md`;
  const dirParts = parts.slice(0, -1);
  return isKo
    ? join(docsMountKo, ...dirParts, finalName)
    : join(docsMountEn, ...dirParts, finalName);
}

async function ensureCleanMount() {
  // A stale mount from a previous run could contain links pointing at
  // renamed source files, so wipe both locale roots and rebuild.
  for (const dir of [docsMountEn, docsMountKo]) {
    try {
      await rm(dir, { recursive: true, force: true });
    } catch {
      // best-effort cleanup
    }
  }
}

async function linkOne(sourceAbs, mountAbs) {
  await mkdir(dirname(mountAbs), { recursive: true });
  const target = relative(dirname(mountAbs), sourceAbs);
  await symlink(target, mountAbs);
}

/**
 * Extract `translation_source_sha` from a Korean file's YAML front-matter.
 * Returns null when the file has no front-matter or the field is absent.
 */
async function recordedSourceSha(koAbsPath) {
  const raw = await readFile(koAbsPath, "utf8");
  if (!raw.startsWith("---\n")) return null;
  const closeIdx = raw.indexOf("\n---\n", 4);
  if (closeIdx === -1) return null;
  const frontmatter = raw.slice(4, closeIdx);
  const match = frontmatter.match(/^translation_source_sha:\s*([0-9a-f]+)\s*$/m);
  return match ? match[1] : null;
}

/** Current `git hash-object` of the English source, or null if the file
 *  or git is unavailable. */
function gitHashObject(filePath) {
  try {
    return execFileSync("git", ["hash-object", filePath], {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    }).trim();
  } catch {
    return null;
  }
}

/**
 * Compute the mounted Starlight route slug for a Korean markdown file.
 * Mirrors mountTargetFor() but returns the slug (no extension, index
 * collapsed) rather than a filesystem path.
 *
 *   goals-and-metrics-ko.md              → ko/reference/roadmap/goals-and-metrics
 *   phases/phase-0-instrumentation-ko.md → ko/reference/roadmap/phases/phase-0-instrumentation
 *   README-ko.md                         → ko/reference/roadmap
 */
function mountSlugForKo(relPath) {
  const parts = relPath.split("/");
  const filename = parts[parts.length - 1];
  const bareName = filename.slice(0, -"-ko.md".length);
  const dirParts = parts.slice(0, -1);
  if (bareName === "README") {
    return ["ko", "reference", "roadmap", ...dirParts].join("/");
  }
  return ["ko", "reference", "roadmap", ...dirParts, bareName].join("/");
}

async function main() {
  const src = await stat(docsSource).catch(() => null);
  if (!src || !src.isDirectory()) {
    console.error(`mount-docs: source directory not found: ${docsSource}`);
    process.exit(1);
  }

  await ensureCleanMount();

  let linked = 0;
  const staleEntries = [];
  for await (const { absPath, relPath } of walkMarkdown(docsSource)) {
    const target = mountTargetFor(relPath);
    if (!target) continue;
    await linkOne(absPath, target);
    linked += 1;

    // For each Korean translation, compare the recorded
    // `translation_source_sha` against the current git hash of its
    // English sibling. Mismatches feed the runtime banner shown on
    // stale ko pages (Banner component override).
    if (relPath.endsWith("-ko.md")) {
      const recordedSha = await recordedSourceSha(absPath);
      if (!recordedSha) continue;
      const enPath = absPath.replace(/-ko\.md$/, ".md");
      const currentSha = gitHashObject(enPath);
      if (!currentSha || recordedSha === currentSha) continue;
      staleEntries.push({
        slug: mountSlugForKo(relPath),
        englishSource: relative(repoRoot, enPath),
        recordedSha,
        currentSha,
      });
    }
  }

  await mkdir(dirname(staleListPath), { recursive: true });
  await writeFile(
    staleListPath,
    `${JSON.stringify(staleEntries, null, 2)}\n`,
    "utf8",
  );

  const relEn = relative(siteRoot, docsMountEn);
  const relKo = relative(siteRoot, docsMountKo);
  console.log(`mount-docs: linked ${linked} markdown files into ${relEn}/ and ${relKo}/`);
  console.log(
    `mount-docs: stale-translations.json lists ${staleEntries.length} ko page(s) whose translation_source_sha is out of date.`,
  );
}

main().catch((err) => {
  console.error("mount-docs: failed —", err);
  process.exit(1);
});
