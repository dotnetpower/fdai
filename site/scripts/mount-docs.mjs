#!/usr/bin/env node
// mount-docs.mjs - link canonical Markdown under docs/ into
// src/content/docs without copying, using symbolic links.
//
// The site presents two content trees, both mounted as read-only
// views over the canonical Markdown:
//
//   docs/user-guide/**       → site/src/content/docs/**           (root locale)
//                              site/src/content/docs/ko/**        (Korean)
//   docs/roadmap/**          → site/src/content/docs/reference/roadmap/**
//                              site/src/content/docs/ko/reference/roadmap/**
//
// User-facing pages (Get Started, Concepts, Guides) sit at the top
// of the URL space; the engineering roadmap moves under /reference/
// where it acts as the source-of-truth reference material.
//
// Why symlinks rather than a custom glob() loader:
// Starlight's docsLoader() is fixed to src/content/docs/. Swapping in
// a raw glob() loader is workable in Astro 5 but the Starlight
// maintainer flagged three regressions with that approach:
// multilingual remark bugs, autogenerated sidebar groups not working,
// and lastUpdated relying on git times inside src/content/docs/.
// Symlinks keep Starlight's default loader - all three features work
// - without duplicating any content into the repo.
//
// The links are the site's presentation layer only. The source of
// truth is docs/**/*.md; edits happen there. Symlink targets are
// gitignored so they never appear in commits or in the
// English-only / translation gates.

import { execFileSync } from "node:child_process";
import { mkdir, readdir, readFile, rm, stat, symlink, unlink, writeFile } from "node:fs/promises";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const siteRoot = resolve(scriptDir, "..");
const repoRoot = resolve(siteRoot, "..");
const contentRoot = resolve(siteRoot, "src", "content", "docs");
const staleListPath = resolve(siteRoot, "src", "data", "stale-translations.json");
// Each run writes the absolute path of every symlink it creates so the
// next run can unlink *only its own outputs* - never touching
// hand-authored files that happen to share a mount root (index.mdx,
// ko/index.mdx, and any future user-guide top-level pages).
const manifestPath = resolve(siteRoot, "src", "data", "mount-manifest.json");

/**
 * The engineering roadmap (docs/roadmap/**) is the source-of-truth
 * design reference for contributors, but most of it is too deep for a
 * product-introduction site. Only the operator- and configuration-
 * facing subset is published; the rest stays on GitHub, where any
 * cross-link to an unmounted doc is automatically rewritten to a
 * GitHub blob URL by src/plugins/rewrite-links.mjs (targetMount == null
 * branch). List entries are bare document names (no locale suffix, no
 * extension). README is kept as the section index. Everything not in
 * this set - deep internals (project-structure, tech-stack,
 * csp-neutrality, execution-model, llm-strategy, prompt-composition,
 * assurance-twin, security-and-identity, cost-model, ...) and the
 * phase build sequence (phases/**) - is intentionally off-site.
 */
const ROADMAP_SITE_ALLOWLIST = new Set([
  "README",
  "goals-and-metrics",
  "risk-classification",
  "operator-console",
  "channels-and-notifications",
  "deploy-and-onboard",
  "deployment-preflight",
  "implementation-plan",
]);

/**
 * Each entry describes one canonical source tree and where its
 * English + Korean files should surface on the site. Adding a new
 * top-level section is a matter of adding one entry here. An optional
 * `include` set filters the tree to a curated allowlist of bare
 * document names; omit it to publish every markdown file.
 */
const MOUNTS = [
  {
    source: resolve(repoRoot, "docs", "user-guide"),
    enPrefix: [],
    koPrefix: ["ko"],
    optional: true,
  },
  {
    source: resolve(repoRoot, "docs", "roadmap"),
    enPrefix: ["reference", "roadmap"],
    koPrefix: ["ko", "reference", "roadmap"],
    optional: false,
    include: ROADMAP_SITE_ALLOWLIST,
  },
];

/**
 * Bare document name for an allowlist comparison: the filename with the
 * locale suffix (-ko) and the .md extension removed.
 */
function bareNameOf(relPath) {
  const filename = relPath.split("/").pop() ?? "";
  return filename.endsWith("-ko.md")
    ? filename.slice(0, -"-ko.md".length)
    : filename.slice(0, -".md".length);
}

/**
 * Recursively walk `dir` and yield `{ absPath, relPath }` for each *.md file.
 * relPath is relative to `dir` so callers can decide where the link lands.
 */
async function* walkMarkdown(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const abs = join(dir, entry.name);
    if (entry.isDirectory()) {
      yield* walkMarkdown(abs);
      continue;
    }
    if (!entry.name.endsWith(".md")) continue;
    yield { absPath: abs, relPath: relative(dir, abs) };
  }
}

/**
 * Compute the mounted path for a source markdown file inside a given
 * mount entry. README(-ko).md collapses to the section index; every
 * other file keeps its filename, stripped of the -ko suffix when
 * mounting under the Korean prefix.
 */
function mountTargetFor(mount, relPath) {
  const parts = relPath.split("/");
  const filename = parts[parts.length - 1];
  const isKo = filename.endsWith("-ko.md");
  const bareName = isKo ? filename.slice(0, -"-ko.md".length) : filename.slice(0, -".md".length);
  const finalName = bareName === "README" ? "index.md" : `${bareName}.md`;
  const dirParts = parts.slice(0, -1);
  const prefix = isKo ? mount.koPrefix : mount.enPrefix;
  return join(contentRoot, ...prefix, ...dirParts, finalName);
}

async function ensureCleanMount() {
  // Read the manifest from the previous run and unlink every symlink
  // it recorded. This is precise: we never delete anything we didn't
  // create ourselves, so hand-authored files under mount roots (like
  // ko/index.mdx sharing the /ko/ prefix with the user-guide mount)
  // survive re-mounts unharmed.
  let previous = [];
  try {
    previous = JSON.parse(await readFile(manifestPath, "utf8"));
  } catch {
    // No manifest yet - first run, nothing to clean.
  }
  for (const p of previous) {
    await unlink(p).catch(() => {});
  }
}

async function linkOne(sourceAbs, mountAbs) {
  await mkdir(dirname(mountAbs), { recursive: true });
  const target = relative(dirname(mountAbs), sourceAbs);
  // The user-guide mount lands in contentRoot itself (enPrefix: []),
  // which ensureCleanMount() cannot wipe without deleting hand-authored
  // files like index.mdx. Unlink first so subsequent runs replace links
  // instead of throwing EEXIST.
  await unlink(mountAbs).catch(() => {});
  await symlink(target, mountAbs);
}

/**
 * Extract `translation_source_sha` from a Korean file's YAML front-matter.
 * Returns null when the file has no front-matter or the field is absent.
 */
async function recordedSourceSha(koAbsPath) {
  const raw = await readFile(koAbsPath, "utf8");
  if (!raw.startsWith("---\n")) return null;
  const closeIdx = raw.indexOf("\n---\n", 4);
  if (closeIdx === -1) return null;
  const frontmatter = raw.slice(4, closeIdx);
  const match = frontmatter.match(/^translation_source_sha:\s*([0-9a-f]+)\s*$/m);
  return match ? match[1] : null;
}

/** Current `git hash-object` of the English source, or null if the file
 *  or git is unavailable. */
function gitHashObject(filePath) {
  try {
    return execFileSync("git", ["hash-object", filePath], {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    }).trim();
  } catch {
    return null;
  }
}

/**
 * Compute the mounted Starlight route slug for a Korean markdown file
 * under a specific mount entry. Mirrors mountTargetFor() but returns
 * the slug (no extension, index collapsed) rather than a filesystem
 * path.
 *
 *   user-guide + concepts/foo-ko.md    → ko/concepts/foo
 *   user-guide + README-ko.md          → ko
 *   roadmap    + phases/foo-ko.md      → ko/reference/roadmap/phases/foo
 *   roadmap    + README-ko.md          → ko/reference/roadmap
 */
function mountSlugForKo(mount, relPath) {
  const parts = relPath.split("/");
  const filename = parts[parts.length - 1];
  const bareName = filename.slice(0, -"-ko.md".length);
  const dirParts = parts.slice(0, -1);
  const suffix = bareName === "README" ? dirParts : [...dirParts, bareName];
  return [...mount.koPrefix, ...suffix].join("/");
}

async function main() {
  await ensureCleanMount();

  let linked = 0;
  const staleEntries = [];
  const createdLinks = [];

  for (const mount of MOUNTS) {
    const src = await stat(mount.source).catch(() => null);
    if (!src || !src.isDirectory()) {
      if (mount.optional) continue;
      console.error(`mount-docs: required source directory missing: ${mount.source}`);
      process.exit(1);
    }

    for await (const { absPath, relPath } of walkMarkdown(mount.source)) {
      // Skip anything outside the curated allowlist when the mount
      // defines one. Deep-internal roadmap docs and the phase sequence
      // stay off the product site; their cross-links resolve to GitHub.
      if (mount.include && !mount.include.has(bareNameOf(relPath))) continue;
      const target = mountTargetFor(mount, relPath);
      if (!target) continue;
      await linkOne(absPath, target);
      createdLinks.push(target);
      linked += 1;

      // For each Korean translation, compare the recorded
      // `translation_source_sha` against the current git hash of its
      // English sibling. Mismatches feed the runtime banner shown on
      // stale ko pages (Banner component override).
      if (relPath.endsWith("-ko.md")) {
        const recordedSha = await recordedSourceSha(absPath);
        if (!recordedSha) continue;
        const enPath = absPath.replace(/-ko\.md$/, ".md");
        const currentSha = gitHashObject(enPath);
        if (!currentSha || recordedSha === currentSha) continue;
        staleEntries.push({
          slug: mountSlugForKo(mount, relPath),
          englishSource: relative(repoRoot, enPath),
          recordedSha,
          currentSha,
        });
      }
    }
  }

  await mkdir(dirname(staleListPath), { recursive: true });
  await writeFile(
    staleListPath,
    `${JSON.stringify(staleEntries, null, 2)}\n`,
    "utf8",
  );
  await writeFile(
    manifestPath,
    `${JSON.stringify(createdLinks, null, 2)}\n`,
    "utf8",
  );

  console.log(`mount-docs: linked ${linked} markdown files across ${MOUNTS.length} tree(s).`);
  console.log(
    `mount-docs: stale-translations.json lists ${staleEntries.length} ko page(s) whose translation_source_sha is out of date.`,
  );
}

main().catch((err) => {
  console.error("mount-docs: failed -", err);
  process.exit(1);
});
