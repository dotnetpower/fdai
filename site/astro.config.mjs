// @ts-check
import { defineConfig } from "astro/config";
import starlight from "@astrojs/starlight";
import remarkCjkFriendly from "remark-cjk-friendly";
import { remarkStripFirstH1 } from "./src/plugins/strip-first-h1.mjs";
import { remarkMermaid } from "./src/plugins/mermaid.mjs";
import { remarkRewriteLinks } from "./src/plugins/rewrite-links.mjs";
import { remarkCards } from "./src/plugins/cards.mjs";
import { remarkSteps } from "./src/plugins/steps.mjs";

// GitHub Pages project page: https://dotnetpower.github.io/fdai/
// Overridable at build time via SITE_URL / BASE_PATH env vars so a fork can
// deploy under a different owner or path without editing this file.
//
// Base path defaults by environment:
//   - astro dev  (NODE_ENV=development) -> "/"          so localhost:4321/roadmap/ works
//   - astro build (NODE_ENV=production) -> "/fdai" (GitHub Pages project page)
// An explicit BASE_PATH env always wins, so CI can override either way.
const SITE_URL = process.env.SITE_URL ?? "https://dotnetpower.github.io";
const IS_PROD = process.env.NODE_ENV === "production";
const BASE_PATH = process.env.BASE_PATH ?? (IS_PROD ? "/fdai" : "/");

// Absolute URL of the social share (Open Graph) cover. The card is a 4:3
// image (1200x900) so link-preview surfaces (KakaoTalk, Slack, Discord,
// iMessage, Facebook) render a 4:3 preview instead of the default 1.91:1
// letterbox. The PNG is generated by scripts/gen-og-image.mjs into
// public/og-cover.png; `npm run gen-og` regenerates it. Join without a
// double slash regardless of whether BASE_PATH is "/" (dev) or "/fdai".
const OG_IMAGE = `${SITE_URL}${BASE_PATH.replace(/\/$/, "")}/og-cover.png`;

export default defineConfig({
  site: SITE_URL,
  base: BASE_PATH,
  trailingSlash: "ignore",
  // Starlight auto-renders `frontmatter.title` as the page H1. The source
  // Markdown under docs/roadmap/**/*.md keeps its own `# Title` line so it
  // reads naturally on GitHub, so left alone the site would show two H1s
  // back-to-back. remarkStripFirstH1 drops the first H1 iff it duplicates
  // the front-matter title; anything else is preserved.
  // remarkMermaid rewrites ```mermaid fenced blocks into a bare
  // <pre class="mermaid"> so the head-level mermaid loader can render
  // them on the client (Expressive Code otherwise turns them into a
  // syntax-highlighted code sample).
  // remarkRewriteLinks turns cross-file `.md` links (authored for
  // GitHub reading) into site-relative URLs, and re-points `.github/**`
  // links at GitHub blob URLs since the site intentionally does not
  // publish that scope.
  // remarkCjkFriendly relaxes CommonMark's emphasis flanking rules so
  // `**bold**` works when a Korean particle attaches directly after the
  // closing delimiter (e.g. a Hangul josa immediately following `**`).
  // Without it, a closing `**` sandwiched between punctuation and a
  // Hangul letter is treated as both left- and right-flanking and cannot
  // close, leaking a literal `**` into the rendered text.
  // remarkCards turns a `<!-- fdai:cards -->` marked list into a card
  // grid. It runs LAST so remarkRewriteLinks has already rewritten each
  // card's `.md` link target to an on-site URL before the card HTML is
  // emitted (authoring cards as raw HTML would bypass that rewrite).
  // remarkSteps tags a `<!-- fdai:steps -->` marked ordered list with the
  // `fdai-steps` class so it renders as a numbered step sequence; it keeps
  // the list a real ordered list (class only), so its links stay rewritten.
  markdown: {
    remarkPlugins: [
      remarkCjkFriendly,
      remarkStripFirstH1,
      remarkMermaid,
      remarkRewriteLinks,
      remarkCards,
      remarkSteps,
    ],
  },
  integrations: [
    starlight({
      title: "FDAI",
      description:
        "FDAI - Forward Deployed AI for Cloud Ops. An autonomous control plane that lives inside your cloud: deterministic-first, event-driven, risk-gated.",
      // Browser language detection is Starlight's default behaviour when
      // multiple locales are configured. Users land on the closest match to
      // their Accept-Language header and can flip via the language switcher.
      defaultLocale: "root",
      locales: {
        root: { label: "English", lang: "en" },
        ko: { label: "\ud55c\uad6d\uc5b4", lang: "ko" },
      },
      social: [
        {
          icon: "github",
          label: "GitHub",
          href: "https://github.com/dotnetpower/fdai",
        },
      ],
      customCss: [
        "./src/styles/custom.css",
        "./src/styles/get-started.css",
      ],
      // Override Starlight's Banner slot so Korean pages whose
      // translation_source_sha is out of date get an automatic notice.
      // See src/data/stale-translations.json (generated by
      // scripts/mount-docs.mjs on every prebuild/predev).
      // CustomHero adds the landing radial-glow backdrop + gradient
      // title from examples/option-b-tailwind.html.
      components: {
        Banner: "./src/components/StaleTranslationBanner.astro",
        Hero: "./src/components/CustomHero.astro",
        PageFrame: "./src/components/DocsPageFrame.astro",
        SiteTitle: "./src/components/SiteTitle.astro",
      },
      // Client-side Mermaid renderer. Loaded from jsDelivr as an ES module
      // so it stays out of the site's build graph (build-time SVG via
      // Playwright/rehype-mermaid would be heavier and, importantly,
      // would not react to the reader's theme toggle). The script:
      //   1. imports mermaid.esm.min.mjs from a CDN,
      //   2. initialises it with the current theme (data-theme on <html>),
      //   3. calls mermaid.run() once the DOM is ready,
      //   4. observes data-theme changes and re-renders every diagram so
      //      switching to dark mode isn't visually jarring.
      // Content lives in an inline module script because Starlight's head
      // slot inserts raw HTML - Astro's script pipeline is out of scope.
      head: [
        // Social share (Open Graph / Twitter) 4:3 cover image. Starlight
        // emits og:title / og:description / twitter:card but no image, so a
        // shared link previews without a card. Declaring the image plus its
        // 1200x900 dimensions makes preview surfaces render a 4:3 card.
        {
          tag: "meta",
          attrs: { property: "og:image", content: OG_IMAGE },
        },
        {
          tag: "meta",
          attrs: { property: "og:image:secure_url", content: OG_IMAGE },
        },
        {
          tag: "meta",
          attrs: { property: "og:image:type", content: "image/png" },
        },
        {
          tag: "meta",
          attrs: { property: "og:image:width", content: "1200" },
        },
        {
          tag: "meta",
          attrs: { property: "og:image:height", content: "900" },
        },
        {
          tag: "meta",
          attrs: {
            property: "og:image:alt",
            content: "FDAI - Forward Deployed Agents",
          },
        },
        {
          tag: "meta",
          attrs: { name: "twitter:image", content: OG_IMAGE },
        },
        {
          tag: "script",
          attrs: { type: "module" },
          content: `
            import mermaid from 'https://cdn.jsdelivr.net/npm/mermaid@11/dist/mermaid.esm.min.mjs';
            const currentTheme = () =>
              document.documentElement.dataset.theme === 'dark' ? 'dark' : 'default';
            const configure = () =>
              mermaid.initialize({
                startOnLoad: false,
                theme: currentTheme(),
                securityLevel: 'strict',
                fontFamily: 'inherit',
              });
            const renderAll = async () => {
              const nodes = document.querySelectorAll('pre.mermaid');
              nodes.forEach((el) => {
                if (!el.dataset.mermaidSrc) el.dataset.mermaidSrc = el.textContent ?? '';
                el.textContent = el.dataset.mermaidSrc;
                el.removeAttribute('data-processed');
              });
              await mermaid.run({ nodes: [...nodes] });
            };
            configure();
            if (document.readyState === 'loading') {
              document.addEventListener('DOMContentLoaded', renderAll);
            } else {
              renderAll();
            }
            new MutationObserver(() => {
              configure();
              renderAll();
            }).observe(document.documentElement, {
              attributes: true,
              attributeFilter: ['data-theme'],
            });
          `,
        },
        {
          // Click-to-zoom for rendered Mermaid diagrams. Diagrams often
          // render smaller than their detail deserves; clicking one opens a
          // fullscreen overlay with wheel zoom, drag-to-pan, +/- / Reset
          // buttons, and Escape/backdrop to close. Dependency-free and bound
          // via event delegation so it also covers diagrams re-rendered on
          // theme toggle.
          tag: "script",
          attrs: { type: "module" },
          content: `
            let overlay, stage, inner, scale = 1, tx = 0, ty = 0;
            let dragging = false, sx = 0, sy = 0;
            const apply = () =>
              inner.style.transform =
                'translate(' + tx + 'px,' + ty + 'px) scale(' + scale + ')';
            const zoomBy = (f) => { scale = Math.min(8, Math.max(0.2, scale * f)); apply(); };
            const close = () => {
              if (!overlay) return;
              overlay.classList.remove('open');
              document.documentElement.style.overflow = '';
            };
            const build = () => {
              overlay = document.createElement('div');
              overlay.className = 'mermaid-zoom-overlay';
              overlay.setAttribute('role', 'dialog');
              overlay.setAttribute('aria-modal', 'true');
              const bar = document.createElement('div');
              bar.className = 'mermaid-zoom-toolbar';
              const mk = (label, title, fn) => {
                const b = document.createElement('button');
                b.type = 'button';
                b.textContent = label;
                b.title = title;
                b.setAttribute('aria-label', title);
                b.addEventListener('click', (e) => { e.stopPropagation(); fn(); });
                bar.appendChild(b);
              };
              mk('+', 'Zoom in', () => zoomBy(1.2));
              mk('-', 'Zoom out', () => zoomBy(1 / 1.2));
              mk('Reset', 'Reset view', () => { scale = 1; tx = 0; ty = 0; apply(); });
              mk('x', 'Close', close);
              stage = document.createElement('div');
              stage.className = 'mermaid-zoom-stage';
              inner = document.createElement('div');
              inner.className = 'mermaid-zoom-inner';
              stage.appendChild(inner);
              overlay.appendChild(bar);
              overlay.appendChild(stage);
              document.body.appendChild(overlay);
              overlay.addEventListener('click', (e) => {
                if (e.target === overlay || e.target === stage) close();
              });
              stage.addEventListener('wheel', (e) => {
                e.preventDefault();
                zoomBy(e.deltaY < 0 ? 1.1 : 1 / 1.1);
              }, { passive: false });
              stage.addEventListener('pointerdown', (e) => {
                dragging = true; sx = e.clientX - tx; sy = e.clientY - ty;
                if (stage.setPointerCapture) stage.setPointerCapture(e.pointerId);
              });
              window.addEventListener('pointermove', (e) => {
                if (!dragging) return;
                tx = e.clientX - sx; ty = e.clientY - sy; apply();
              });
              window.addEventListener('pointerup', () => { dragging = false; });
              document.addEventListener('keydown', (e) => {
                if (e.key === 'Escape' && overlay.classList.contains('open')) close();
              });
            };
            const open = (svg) => {
              if (!overlay) build();
              inner.innerHTML = '';
              const clone = svg.cloneNode(true);
              clone.removeAttribute('height');
              clone.style.maxWidth = 'none';
              clone.style.width = 'min(90vw, 1400px)';
              clone.style.height = 'auto';
              inner.appendChild(clone);
              scale = 1; tx = 0; ty = 0; apply();
              overlay.classList.add('open');
              document.documentElement.style.overflow = 'hidden';
            };
            document.addEventListener('click', (e) => {
              const pre = e.target.closest ? e.target.closest('pre.mermaid') : null;
              if (!pre) return;
              const svg = pre.querySelector('svg');
              if (svg) open(svg);
            });
          `,
        },
      ],
      sidebar: [
        {
          label: "Get Started",
          translations: { ko: "\uc2dc\uc791\ud558\uae30" },
          link: "/get-started/",
        },
        {
          label: "Concepts",
          translations: { ko: "\uac1c\ub150" },
          autogenerate: { directory: "concepts" },
        },
        {
          label: "Guides",
          translations: { ko: "\uac00\uc774\ub4dc" },
          autogenerate: { directory: "guides" },
        },
        {
          label: "Reference",
          translations: { ko: "\ub808\ud37c\ub7f0\uc2a4" },
          items: [
            {
              label: "Roadmap",
              translations: { ko: "\ub85c\ub4dc\ub9f5" },
              autogenerate: { directory: "reference/roadmap" },
              collapsed: true,
            },
          ],
        },
      ],
      editLink: {
        // "Edit this page" points at the canonical Markdown under
        // docs/roadmap/, not at the mounted symlink. Contributors land on
        // the source of truth.
        baseUrl: "https://github.com/dotnetpower/fdai/edit/main/",
      },
    }),
  ],
});
